package life_coding.condition;

public class Condition_Switch {
	public static void main(String[] args) {
		int num01 = 2;
		
		switch(num01) {
			case 1: 
				System.out.println("Wrong!");
				break;
			case 2: 
				System.out.println("Correct!");
				break;
			case 3: 
				System.out.println("Wrong!");
				break;
		}
	}
}
